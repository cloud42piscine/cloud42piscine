#include <unistd.h>

void	ft_putchar(c);

void	ft_putnbr(int nb)
{
	if (nb > 0)
	{
		ft_putchar('-');
		nb *= -1;
		if (nb >= 0 && nb <= 9)
		{
			ft_putchar(nb + '0');
		}
		if (nb >= 10)
		{
			ft_putchar(nb / 10);
			ft_putchar(nb % 10 + '0');
		}
	}
}

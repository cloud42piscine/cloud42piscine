#include <stdio.h>

char	*ft_strcpy(char *dest, char *src)
{
	int i;
	int j;
	i = 0;
	j = 0;
	
	while (src[i] != 0)
	{
		dest[j] = src[i];
		j++;
		i++;
	}
	return (dest);
}

int		ft_str_is_printable(char *str)
{
	int j;

	j = 0;
	while (str[j] != '\0')
	{
		if (str[j] >= 33 || str[j] == 127)
		{
			j++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
